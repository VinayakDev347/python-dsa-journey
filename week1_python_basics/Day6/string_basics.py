# String Basics

text = "python"
print(text)

# Indexing
print(text[0])
print(text[-1])

# Looping through string
for i in text:
    print(i)

# Length
print(len(text))

# String methods
print(text.upper())     # full capital letter

print(text.lower())     # full small letter

print(text.replace("p","Z"))